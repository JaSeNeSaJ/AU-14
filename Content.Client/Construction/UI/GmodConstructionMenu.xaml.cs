    private const string LogoPath = "/Textures/Logo/logo.png";

    // Mirror the presenter's private category-name constants so we can fire the right events.
    private const string FavoriteCatName = "construction-category-favorites";
    private const string ForAllCategoryName = "construction-category-all";

    // Palette: the whole menu chrome is SOLID; only the center item grid is low-opacity so the
    // game world shows through there (and nowhere else). For that to work, every layer BEHIND the
    // center grid (the window background panel and RootPanel) must be transparent, while the
    // surrounding panels paint their own solid backgrounds.
    // USCM Amber Terminal palette (see memory project-ui-palette). Chrome panels are the dark amber-tinted
    // panel color; the center grid stays low-opacity near-black so the game world shows through there.
    // Modern dark-slate palette (the USCM amber look was dropped). Flat: StyleBoxFlat can't round corners,
    // so true rounded edges would need 9-slice texture assets (deferred art pass).
    private static readonly Color RootBg = Color.Transparent;
    private static readonly Color PanelBg = Color.FromHex("#1E222B");     // side panels (solid)
    private static readonly Color CenterBg = Color.FromHex("#12141A99");  // see-through center grid
    private static readonly Color DetailBg = Color.FromHex("#1A1E26");
    private static readonly Color RowBg = Color.FromHex("#232833");       // list/button rows
    private static readonly Color RowHover = Color.FromHex("#2D3340");
    private static readonly Color RowSelected = Color.FromHex("#2B3A57"); // accent-tinted selection
    private static readonly Color Accent = Color.FromHex("#4C8DFF");      // modern blue accent
    private static readonly Color SubtleBorder = Color.FromHex("#2A2F3A");
    // Regular spawnlist buttons share one blue-tinted color so they read as a single group; the synthetic
    // "Saved Builds" and "Favorites" buttons stay the neutral RowBg so they stand apart from the spawnlists.
    private static readonly Color SpawnlistBg = Color.FromHex("#28324A");
    private static readonly Color SpawnlistSelected = Color.FromHex("#33476E");

    /// <summary>A modern flat panel stylebox (subtle border) over the given background.</summary>
    private static StyleBoxFlat Panel(Color bg) => new()
    {
        BackgroundColor = bg,
        BorderColor = SubtleBorder,
        BorderThickness = new Thickness(1),
    };

    /// <summary>A flat button stylebox with comfortable padding.</summary>
    private static StyleBoxFlat ButtonBox(Color bg, Color border) => new()
    {
        BackgroundColor = bg,
        BorderColor = border,
        BorderThickness = new Thickness(1),
        Padding = new Thickness(8, 5, 8, 5),
    };

    /// <summary>
    /// Applies the modern flat look to a button per-instance - <see cref="ContainerButton.StyleBoxOverride"/>
    /// bypasses the global stylesheet - with a hover swap and an accent-tinted selected state for toggles.
    /// </summary>
    private void Modernize(ContainerButton btn, bool toggle = false, Color? baseBg = null, Color? selectedBg = null)
    {
        var normal = baseBg ?? RowBg;
        var selectedColor = selectedBg ?? RowSelected;

        void Refresh()
        {
            var selected = toggle && btn.Pressed;
            btn.StyleBoxOverride = ButtonBox(selected ? selectedColor : normal, selected ? Accent : SubtleBorder);
        }

        btn.OnMouseEntered += _ =>
        {
            if (!btn.Disabled && !(toggle && btn.Pressed))
                btn.StyleBoxOverride = ButtonBox(RowHover, SubtleBorder);
        };
        btn.OnMouseExited += _ => Refresh();
        if (toggle)
            btn.OnToggled += _ => Refresh();
        Refresh();
    }

