    private readonly ListContainer _stubRecipes = new();
    private readonly OptionButton _stubOptionCategories = new();
    private readonly GridContainer _stubGrid = new();

    private string[] _categories = Array.Empty<string>();
    private string _currentTreeCategory = ForAllCategoryName;

    // Spawnlist tree (left panel). One toggle button per spawnlist; AU14 is the default group.
    private const string DefaultSpawnlist = "AU14";
    // The right-hand top-bar tab ("Z-Level (Experimental)") is a separate PAGE that gathers the experimental
    // z-level spawnlists (the buildable Tiles and the ZLevel stairs/beams); the Spawnlists page shows everything
    // else. The two top-bar tabs flip between these pages.
    private const string TilesSpawnlist = "Tiles";

    // The z-level page's spawnlist set lives in one shared place (Content.Client._AU14.Construction) so the
    // presenter's "All" view can exclude the very same spawnlists this page gathers - keeping the two pages
    // as separate main categories that never share constructs.
    private static bool IsZLevelPageSpawnlist(string spawnlist) =>
        _AU14.Construction.ZLevelPageSpawnlists.Contains(spawnlist);
    private readonly Dictionary<string, BaseButton> _spawnlistButtons = new();
    private string _selectedSpawnlist = DefaultSpawnlist;
    private Texture? _logoTexture;

    // Which top-bar page is shown. Spawnlists = every spawnlist except Tiles; Tiles = only the Tiles spawnlist.
    private enum MenuPage { Spawnlists, Tiles }
    private MenuPage _currentPage = MenuPage.Spawnlists;
    private IReadOnlyList<string> _allSpawnlists = Array.Empty<string>();

    // "Saved Builds" - a synthetic spawnlist that lists user-saved builds instead of recipes.
    private BaseButton? _savedBuildsButton;
    private bool _savedBuildsActive;
    private SavedBuildInfo? _currentSavedBuild;
    private readonly SavedBuildListSystem _savedBuildList;

    // Admin-only recipe management (detail panel "Change Recipe" / "Remove Item"). The target entity id drives
    // Change Recipe (opens the editor for it); the construction id drives Remove Item (hides that recipe).
    private bool _isAdmin;
    private string? _currentTargetEntityId;
    private string? _currentRecipeId;

    public GmodConstructionMenu()
    {
        SetSize = new Vector2(1100, 640);
        MinSize = new Vector2(900, 500);

        IoCManager.InjectDependencies(this);
        RobustXamlLoader.Load(this);

        Title = Loc.GetString("construction-menu-title");

        _savedBuildList = IoCManager.Resolve<IEntitySystemManager>().GetEntitySystem<SavedBuildListSystem>();
        _savedBuildList.ListUpdated += OnSavedBuildListUpdated;

        // DefaultWindow draws its own opaque background panel (the "windowPanel" styled
        // PanelContainer at child index 0) BEHIND our content. Make it transparent so our
        // semi-transparent panels are the only thing tinting the game world behind the menu.
        if (GetChild(0) is PanelContainer windowPanel)
        {
            windowPanel.PanelOverride = new StyleBoxFlat
            {
                BackgroundColor = Color.Transparent,
                BorderColor = Color.Transparent,
                BorderThickness = new Thickness(0f),
            };
        }

        // Solid chrome everywhere except the center item grid, which is the only low-opacity panel.
        RootPanel.PanelOverride = new StyleBoxFlat { BackgroundColor = RootBg };
        TopBarPanel.PanelOverride = Panel(PanelBg);
        TreePanel.PanelOverride = Panel(PanelBg);
        CenterPanel.PanelOverride = new StyleBoxFlat { BackgroundColor = CenterBg };
        ToolsPanel.PanelOverride = Panel(PanelBg);
        // Detail abuts Tools on its right: drop the right border so the two read as one continuous surface.
        DetailPanel.PanelOverride = new StyleBoxFlat
        {
            BackgroundColor = DetailBg,
            BorderColor = SubtleBorder,
            BorderThickness = new Thickness(1, 1, 0, 1),
        };

        // Remove the native DefaultWindow title bar - our own slim top bar (Spawnlists tab + mode + close)
        // lives in TopBarPanel. The window stays draggable from its top edge.
        if (GetChild(1) is BoxContainer windowVBox && windowVBox.ChildCount > 0)
            windowVBox.GetChild(0).Visible = false;

        HeaderCloseButton.OnPressed += _ => Close();

        // Apply the modern flat button look to the XAML-defined buttons (per-instance, so it doesn't touch
        // the rest of the game's UI). Toggles get an accent-tinted selected state.
        // The Spawnlists / Tiles tabs act as mutually-exclusive page tabs (neither is greyed out).
        Modernize(TabSpawnlists, toggle: true);
        Modernize(TabTiles, toggle: true);
        TabSpawnlists.OnToggled += args =>
        {
            if (!args.Pressed) { TabSpawnlists.Pressed = true; return; } // can't deselect the active page
            SelectPage(MenuPage.Spawnlists);
        };
        TabTiles.OnToggled += args =>
        {
            if (!args.Pressed) { TabTiles.Pressed = true; return; }
            SelectPage(MenuPage.Tiles);
        };
        Modernize(ModeToggleButton, toggle: true);
        Modernize(HeaderCloseButton);
        Modernize(AdvancedDuplicatorButton);
        Modernize(ConstructionItemsEditorButton);
        Modernize(TilesEditorButton);
        Modernize(LatheEditorButton);
        Modernize(BuildButton, toggle: true);
        Modernize(FavoriteButton);
        Modernize(EraseButton, toggle: true);
        Modernize(ClearButton);
        Modernize(TreeFavorites, toggle: true);
        Modernize(RecipeChangeButton);
        Modernize(RecipeRemoveButton);
        Modernize(SavedBuildDeleteButton);
        Modernize(SavedBuildFolderButton);

        // Admin recipe management (detail panel). Change Recipe opens the editor for the recipe's target
        // entity; Remove Item hides the recipe from the menu (works for vanilla recipes too).
        RecipeChangeButton.Text = Loc.GetString("gmod-construction-menu-change-recipe");
        RecipeRemoveButton.Text = Loc.GetString("gmod-construction-menu-remove-item");
        RecipeChangeButton.OnPressed += _ =>
        {
            if (_currentTargetEntityId is { } targetId)
                IoCManager.Resolve<IEntitySystemManager>()
                    .GetEntitySystem<_AU14.Construction.CustomConstruction.CustomConstructionEditorClientSystem>()
                    .RequestChangeRecipe(targetId);
        };
        RecipeRemoveButton.OnPressed += _ =>
        {
            if (_currentRecipeId is not { } recipeId)
                return;

            IoCManager.Resolve<IEntitySystemManager>()
                .GetEntitySystem<_AU14.Construction.CustomConstruction.CustomConstructionEditorClientSystem>()
                .HideRecipe(recipeId);
            CloseDetailPanel();
            // Repopulate so the just-hidden recipe disappears from the grid immediately.
            PopulateRecipes?.Invoke(this, (SearchBar.Text, _currentTreeCategory));
        };

        // Saved-build management (detail panel). Delete removes the selected build; the folder button opens
        // the saved-builds directory in the host OS file explorer.
        SavedBuildDeleteButton.Text = Loc.GetString("gmod-construction-menu-delete-build");
        SavedBuildFolderButton.Text = Loc.GetString("gmod-construction-menu-open-build-folder");
        SavedBuildDeleteButton.OnPressed += _ =>
        {
            if (_currentSavedBuild is not { } saved)
                return;

            _savedBuildList.Delete(saved.Id);
            _currentSavedBuild = null;
            CloseDetailPanel();
            // The server replies with a refreshed list (ListUpdated -> PopulateSavedBuilds).
        };
        SavedBuildFolderButton.OnPressed += _ => _savedBuildList.OpenFolder();

        // Recolor the static category headers (e.g. "Your Spawnlists", "Construction", "Admin Tools") to
        // the accent. Recipe-mode headers (built by the presenter) are recolored in FrameUpdate as they
        // appear; saved-build headers set their own accent color on creation.
        RecolorKeyLabels(TreePanel);
        RecolorKeyLabels(ToolsPanel);

        // The detail panel collapses smoothly; clip children while it is narrower than its content.
        DetailPanel.RectClipContent = true;

        // Preload the AU14 logo shown next to the AU14 spawnlist entry. (XAML cannot set Texture directly.)
        var cache = IoCManager.Resolve<IResourceCache>();
        try
        {
            _logoTexture = cache.GetResource<TextureResource>(LogoPath).Texture;
        }
        catch
        {
            // Missing texture is non-fatal; the label alone is fine.
        }

        TreeFavorites.OnToggled += args =>
        {
            if (!args.Pressed)
            {
                if (_currentTreeCategory == FavoriteCatName)
                    TreeFavorites.Pressed = true;
                return;
            }

            // Favorites spans all spawnlists; deselect the spawnlist buttons.
            foreach (var b in _spawnlistButtons.Values)
                b.Pressed = false;
            DeactivateSavedBuilds();

            _currentTreeCategory = FavoriteCatName;
            PopulateRecipes?.Invoke(this, (SearchBar.Text, FavoriteCatName));
        };

        SearchBar.OnTextChanged += _ =>
        {
            if (_savedBuildsActive)
                PopulateSavedBuilds();
            else
                PopulateRecipes?.Invoke(this, (SearchBar.Text, _currentTreeCategory));
        };

        BuildButton.Text = Loc.GetString("construction-menu-place-ghost");
        BuildButton.OnToggled += args =>
        {
            // Saved builds reuse the detail panel's Build button as a "Place" action (not a toggle).
            if (_currentSavedBuild is { } saved)
            {
                BuildButton.Pressed = false;
                if (args.Pressed)
                {
                    IoCManager.Resolve<IEntitySystemManager>()
                        .GetEntitySystem<SavedBuildPlacementSystem>()
                        .BeginPlacement(saved);
                    // Close the menu so the cursor is free to choose the placement location.
                    Close();
                }
                return;
            }

            BuildButtonToggled?.Invoke(this, args.Pressed);
        };

        EraseButton.Text = Loc.GetString("construction-menu-eraser-mode");
        EraseButton.OnToggled += args => EraseButtonToggled?.Invoke(this, args.Pressed);

        ClearButton.Text = Loc.GetString("construction-menu-clear-all");
        ClearButton.OnPressed += _ => ClearAllGhosts?.Invoke(this, EventArgs.Empty);

        FavoriteButton.OnPressed += _ =>
        {
            // For saved builds the Favorite button is repurposed as "Place at Original".
            if (_currentSavedBuild is { } saved)
            {
                IoCManager.Resolve<IEntitySystemManager>()
                    .GetEntitySystem<SavedBuildPlacementSystem>()
                    .PlaceAtOriginal(saved);
                Close();
                return;
            }

            RecipeFavorited?.Invoke(this, EventArgs.Empty);
        };

        // Admin/Player build-mode toggle. Admins default to instant/free placement, but can opt into the
        // player flow (vanilla construction ghosts + materials) - useful when admins are playing normally.
        // Non-admins can only ever build via ghosts, so the toggle is forced on and disabled for them.
        _isAdmin = IoCManager.Resolve<Administration.Managers.IClientAdminManager>().IsAdmin();
        var isAdmin = _isAdmin;
        ModeToggleButton.Pressed = !isAdmin;
        ModeToggleButton.Disabled = !isAdmin;
        ApplyBuildMode();
        ModeToggleButton.OnToggled += _ => ApplyBuildMode();

        // Construction → AdvancedAtkinsonatorv2: opens the build-save selection window (save your builds).
        AdvancedDuplicatorButton.OnPressed += _ =>
        {
            IoCManager.Resolve<IEntitySystemManager>()
                .GetEntitySystem<Content.Client._AU14.SavedBuilds.BuildSaveModeSystem>()
                .ToggleWindow();
            Close();
        };

        // Admin Tools → Construction Items Editor. Visible to everyone; the client system
        // pops up "not an admin" for non-admins and otherwise opens the entity selector → editor.
        ConstructionItemsEditorButton.OnPressed += _ =>
            IoCManager.Resolve<IEntitySystemManager>()
                .GetEntitySystem<_AU14.Construction.CustomConstruction.CustomConstructionEditorClientSystem>()
                .OpenItemsEditor();

        // Admin Tools → Tiles Editor (add a tile to the menu) and Lathe Editor (add a lathe print recipe).
        TilesEditorButton.OnPressed += _ =>
            IoCManager.Resolve<IEntitySystemManager>()
                .GetEntitySystem<_AU14.Construction.CustomConstruction.CustomConstructionEditorClientSystem>()
                .OpenTilesEditor();
        LatheEditorButton.OnPressed += _ =>
            IoCManager.Resolve<IEntitySystemManager>()
                .GetEntitySystem<_AU14.Construction.CustomConstruction.CustomConstructionEditorClientSystem>()
                .OpenLatheEditor();

        // The spawnlist buttons (and initial selection) are built once the presenter calls SetSpawnlists.
    }

