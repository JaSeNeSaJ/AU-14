    /// <summary>The spawnlist currently selected in the left tree.</summary>
    public string SelectedSpawnlist => _selectedSpawnlist;

    /// <summary>Rebuilds the left-tree spawnlist buttons from the presenter's discovered spawnlists.</summary>
    public void SetSpawnlists(IReadOnlyList<string> spawnlists)
    {
        _allSpawnlists = spawnlists;

        // Keep a valid selection for the Spawnlists page (never the Tiles-only entry).
        if (_selectedSpawnlist == TilesSpawnlist || !spawnlists.Contains(_selectedSpawnlist))
            _selectedSpawnlist = spawnlists.FirstOrDefault(s => s != TilesSpawnlist) ?? DefaultSpawnlist;

        BuildTree();

        // Reflect the current selection in the buttons without re-firing population.
        if (_currentPage == MenuPage.Tiles)
        {
            if (_spawnlistButtons.TryGetValue(TilesSpawnlist, out var tilesButton))
                tilesButton.Pressed = true;
        }
        else if (_currentTreeCategory != FavoriteCatName && !_savedBuildsActive)
        {
            foreach (var (name, button) in _spawnlistButtons)
                button.Pressed = name == _selectedSpawnlist;
        }

        RecolorKeyLabels(TreePanel);
    }

    /// <summary>
    /// Rebuilds the left-tree contents for the current page: the Spawnlists page lists every spawnlist except
    /// Tiles (plus the synthetic Saved Builds entry and Favorites), while the Tiles page lists only Tiles.
    /// </summary>
    private void BuildTree()
    {
        SpawnlistContainer.RemoveAllChildren();
        _spawnlistButtons.Clear();
        _savedBuildsButton = null;

        if (_currentPage == MenuPage.Tiles)
        {
            // The "Z-Level (Experimental)" page gathers the Tiles and ZLevel spawnlists together.
            foreach (var spawnlist in _allSpawnlists)
            {
                if (!IsZLevelPageSpawnlist(spawnlist))
                    continue;

                var button = MakeSpawnlistButton(spawnlist);
                _spawnlistButtons[spawnlist] = button;
                SpawnlistContainer.AddChild(button);
            }

            // Favorites and Saved Builds belong to the Spawnlists page only.
            TreeFavorites.Visible = false;
            return;
        }

        foreach (var spawnlist in _allSpawnlists)
        {
            if (IsZLevelPageSpawnlist(spawnlist))
                continue;

            var button = MakeSpawnlistButton(spawnlist);
            _spawnlistButtons[spawnlist] = button;
            SpawnlistContainer.AddChild(button);
        }

        // Append the synthetic "Saved Builds" spawnlist (player-saved builds, not recipes).
        _savedBuildsButton = MakeSavedBuildsButton();
        SpawnlistContainer.AddChild(_savedBuildsButton);

        TreeFavorites.Visible = Array.IndexOf(_categories, FavoriteCatName) >= 0;
    }

    /// <summary>Flips between the Spawnlists and Tiles pages, rebuilding the tree and selecting a default.</summary>
    private void SelectPage(MenuPage page)
    {
        _currentPage = page;
        TabSpawnlists.Pressed = page == MenuPage.Spawnlists;
        TabTiles.Pressed = page == MenuPage.Tiles;

        _currentTreeCategory = ForAllCategoryName;
        TreeFavorites.Pressed = false;
        DeactivateSavedBuilds();

        BuildTree();
        RecolorKeyLabels(TreePanel);

        string target;
        if (page == MenuPage.Tiles)
        {
            // Prefer the ZLevel spawnlist on this page, falling back to Tiles or whatever is present.
            target = _spawnlistButtons.ContainsKey("ZLevel")
                ? "ZLevel"
                : (_spawnlistButtons.Keys.FirstOrDefault() ?? TilesSpawnlist);
        }
        else
        {
            target = _spawnlistButtons.ContainsKey(_selectedSpawnlist)
                ? _selectedSpawnlist
                : (_spawnlistButtons.Keys.FirstOrDefault() ?? DefaultSpawnlist);
        }

        _selectedSpawnlist = target;
        foreach (var (name, button) in _spawnlistButtons)
            button.Pressed = name == target;

        PopulateRecipes?.Invoke(this, (SearchBar.Text, ForAllCategoryName));
    }

    private BaseButton MakeSpawnlistButton(string spawnlist)
    {
        BaseButton button;
        if (spawnlist == DefaultSpawnlist && _logoTexture != null)
        {
            // AU14 gets the emblem next to its label. A ContainerButton has no stylesheet background of
            // its own (unlike a plain Button), so wrap the content in a PanelContainer to give it the same
            // themed, amber-framed background as the other spawnlist entries.
            var containerButton = new ContainerButton
            {
                ToggleMode = true,
                HorizontalExpand = true,
                Margin = new Thickness(0, 0, 0, 2),
            };
            // Use the shared spawnlist color so the AU14 entry matches the other spawnlist buttons, and swap to
            // the selected tint on toggle (a ContainerButton has no stylesheet background of its own).
            var panel = new PanelContainer
            {
                PanelOverride = Panel(SpawnlistBg),
                Children =
                {
                    new BoxContainer
                    {
                        Orientation = BoxContainer.LayoutOrientation.Horizontal,
                        Margin = new Thickness(6, 4, 6, 4),
                        Children =
                        {
                            // Greyscale (desaturated) AU14 emblem - see GreyscaleTextureRect.
                            new GreyscaleTextureRect
                            {
                                Texture = _logoTexture,
                                SetSize = new Vector2(18, 18),
                                Margin = new Thickness(0, 0, 6, 0),
                                VerticalAlignment = Control.VAlignment.Center,
                            },
                            new Label { Text = spawnlist, VerticalAlignment = Control.VAlignment.Center },
                        },
                    },
                },
            };
            containerButton.AddChild(panel);
            containerButton.OnToggled += _ =>
                panel.PanelOverride = Panel(containerButton.Pressed ? SpawnlistSelected : SpawnlistBg);
            button = containerButton;
        }
        else
        {
            var plain = new Button
            {
                Text = spawnlist,
                ToggleMode = true,
                HorizontalExpand = true,
                Margin = new Thickness(0, 0, 0, 2),
            };
            // Shared spawnlist color (distinct from the neutral Saved Builds / Favorites buttons).
            Modernize(plain, toggle: true, SpawnlistBg, SpawnlistSelected);
            button = plain;
        }

        button.OnToggled += args =>
        {
            if (!args.Pressed)
            {
                // Don't allow deselecting the active spawnlist by clicking it again.
                if (_selectedSpawnlist == spawnlist && _currentTreeCategory != FavoriteCatName)
                    button.Pressed = true;
                return;
            }

            _selectedSpawnlist = spawnlist;
            _currentTreeCategory = ForAllCategoryName;

            foreach (var (name, other) in _spawnlistButtons)
            {
                if (name != spawnlist)
                    other.Pressed = false;
            }
            TreeFavorites.Pressed = false;
            DeactivateSavedBuilds();

            PopulateRecipes?.Invoke(this, (SearchBar.Text, ForAllCategoryName));
        };

        return button;
    }

    // -------------------------------------------------------------------------
    // Saved Builds spawnlist
    // -------------------------------------------------------------------------

    private BaseButton MakeSavedBuildsButton()
    {
        var button = new Button
        {
            Text = Loc.GetString("gmod-construction-menu-saved-builds"),
            ToggleMode = true,
            HorizontalExpand = true,
            Margin = new Thickness(0, 4, 0, 2),
        };
        Modernize(button, toggle: true);

        button.OnToggled += args =>
        {
            if (!args.Pressed)
            {
                if (_savedBuildsActive)
                    button.Pressed = true; // can't deselect the active list by re-clicking
                return;
            }

            foreach (var other in _spawnlistButtons.Values)
                other.Pressed = false;
            TreeFavorites.Pressed = false;

            _savedBuildsActive = true;
            _currentSavedBuild = null;
            CloseDetailPanel();
            _savedBuildList.Refresh();
            PopulateSavedBuilds();
        };

        return button;
    }

    /// <summary>Pushes the current toggle state to the placement system and updates the button label.</summary>
    private void ApplyBuildMode()
    {
        var playerMode = ModeToggleButton.Pressed;
        IoCManager.Resolve<IEntitySystemManager>()
            .GetEntitySystem<SavedBuildPlacementSystem>()
            .ForcePlayerMode = playerMode;
        ModeToggleButton.Text = Loc.GetString(
            playerMode ? "gmod-construction-menu-mode-player" : "gmod-construction-menu-mode-admin");
    }

    private void DeactivateSavedBuilds()
    {
        _savedBuildsActive = false;
        _currentSavedBuild = null;
        if (_savedBuildsButton != null)
            _savedBuildsButton.Pressed = false;
    }

    private void OnSavedBuildListUpdated()
    {
        if (_savedBuildsActive)
            PopulateSavedBuilds();
    }

    /// <summary>Fills the center grid with saved-build cards grouped by source grid/map name.</summary>
    private void PopulateSavedBuilds()
    {
        GroupedRecipesContainer.RemoveAllChildren();

        var search = SearchBar.Text.Trim();
        var builds = _savedBuildList.Builds.Where(b =>
            string.IsNullOrEmpty(search) || b.Name.Contains(search, StringComparison.InvariantCultureIgnoreCase));

        var anyShown = false;
        foreach (var group in builds
                     .GroupBy(b => string.IsNullOrEmpty(b.Source) ? "Unknown" : b.Source)
                     .OrderBy(g => g.Key, StringComparer.InvariantCultureIgnoreCase))
        {
            GroupedRecipesContainer.AddChild(new Label
            {
                Text = group.Key,
                StyleClasses = { "LabelKeyText" },
                FontColorOverride = Accent,
                Margin = new Thickness(2, 6, 0, 2),
            });

            var grid = new GridContainer { Columns = 1, HorizontalExpand = true };
            foreach (var build in group)
            {
                var info = build;
                var card = new Button
                {
                    Text = Loc.GetString("saved-build-card",
                        ("name", info.Name), ("author", info.Author), ("count", info.EntityCount)),
                    HorizontalExpand = true,
                    Margin = new Thickness(0, 0, 0, 2),
                };
                Modernize(card);
                card.OnPressed += _ => ShowSavedBuildDetail(info);
                grid.AddChild(card);
                anyShown = true;
            }

            GroupedRecipesContainer.AddChild(grid);
        }

        if (!anyShown)
        {
            GroupedRecipesContainer.AddChild(new Label
            {
                Text = Loc.GetString("saved-build-none"),
                Margin = new Thickness(4),
            });
        }
    }

    private void ShowSavedBuildDetail(SavedBuildInfo info)
    {
        _currentSavedBuild = info;

        TargetTexture.SetPrototype(null);
        TargetTexture.Modulate = Color.White;
        TargetName.SetMessage(info.Name);
        TargetDesc.SetMessage(Loc.GetString("saved-build-detail-desc",
            ("author", info.Author),
            ("count", info.EntityCount),
            ("source", string.IsNullOrEmpty(info.Source) ? "Unknown" : info.Source)));
        RecipeStepList.Clear();
        FavoriteButton.Visible = true;
        FavoriteButton.Text = Loc.GetString("saved-build-place-original-button");
        BuildButton.Disabled = false;
        BuildButton.Pressed = false;
        BuildButton.Text = Loc.GetString("saved-build-place-button");

        // Saved-build detail: show Delete (server validates admin/author) and, for admins, the open-folder
        // button. Recipe-management buttons and the ghost erase/clear row don't apply to saved builds.
        _currentTargetEntityId = null;
        _currentRecipeId = null;
        RecipeChangeButton.Visible = false;
        RecipeRemoveButton.Visible = false;
        SavedBuildDeleteButton.Visible = true;
        SavedBuildFolderButton.Visible = _isAdmin;
        GhostActionsRow.Visible = false;

        OpenDetailPanel();
    }

    protected override void Dispose(bool disposing)
    {
        base.Dispose(disposing);
        if (disposing)
            _savedBuildList.ListUpdated -= OnSavedBuildListUpdated;
    }

    private int _lastGroupCount = -1;

    /// <summary>Recursively recolors every "LabelKeyText" category header under <paramref name="root"/> to the accent.</summary>
    private void RecolorKeyLabels(Control root)
    {
        foreach (var child in root.Children)
        {
            if (child is Label label && label.HasStyleClass("LabelKeyText"))
                label.FontColorOverride = Accent;
            RecolorKeyLabels(child);
        }
    }

    protected override void FrameUpdate(FrameEventArgs args)
    {
        base.FrameUpdate(args);

        // Recipe-mode category headers are built by the shared presenter (no callback), so recolor them
        // whenever the grouped grid's contents change.
        if (GroupedRecipesContainer.ChildCount != _lastGroupCount)
        {
            _lastGroupCount = GroupedRecipesContainer.ChildCount;
            RecolorKeyLabels(GroupedRecipesContainer);
        }

        if (Math.Abs(_detailCurrentWidth - _detailGoalWidth) < 0.5f)
        {
            _detailCurrentWidth = _detailGoalWidth;
            if (_detailGoalWidth <= 0f && DetailPanel.Visible)
                DetailPanel.Visible = false;
            return;
        }

        var step = DetailSlideSpeed * args.DeltaSeconds;
        if (_detailCurrentWidth < _detailGoalWidth)
            _detailCurrentWidth = Math.Min(_detailGoalWidth, _detailCurrentWidth + step);
        else
            _detailCurrentWidth = Math.Max(_detailGoalWidth, _detailCurrentWidth - step);

        DetailPanel.SetWidth = _detailCurrentWidth;
    }

    private void OpenDetailPanel()
    {
        DetailPanel.Visible = true;
        _detailGoalWidth = DetailTargetWidth;
    }

    private void CloseDetailPanel()
    {
        _detailGoalWidth = 0f;
    }

