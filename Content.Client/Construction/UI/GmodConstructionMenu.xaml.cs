    public void SetRecipeInfo(
        string name,
        string description,
        EntityPrototype? targetPrototype,
        Color iconColor,
        bool isItem,
        bool isFavorite,
        ConstructionPrototype prototype)
    {
        _currentSavedBuild = null;
        BuildButton.Disabled = false;
        BuildButton.Text = Loc.GetString(isItem ? "construction-menu-place-ghost" : "construction-menu-craft");
        TargetName.SetMessage(name);
        TargetDesc.SetMessage(description);
        TargetTexture.SetPrototype(targetPrototype?.ID);
        TargetTexture.Modulate = iconColor;
        FavoriteButton.Visible = true;
        FavoriteButton.Text = Loc.GetString(
            isFavorite ? "construction-add-favorite-button" : "construction-remove-from-favorite-button");

        // Recipe-mode detail: admins get Change Recipe / Remove Item for ANY recipe (vanilla included). The
        // saved-build management buttons stay hidden; the ghost erase/clear row is relevant here.
        _currentTargetEntityId = targetPrototype?.ID;
        _currentRecipeId = prototype.ID;
        RecipeChangeButton.Visible = _isAdmin && _currentTargetEntityId != null;
        RecipeRemoveButton.Visible = _isAdmin;
        SavedBuildDeleteButton.Visible = false;
        SavedBuildFolderButton.Visible = false;
        GhostActionsRow.Visible = true;

        OpenDetailPanel();

        // RMC14
        if (prototype.RMCPrototype != null)
            BuildButton.Text = Loc.GetString("rmc-construction-build-here");
    }

